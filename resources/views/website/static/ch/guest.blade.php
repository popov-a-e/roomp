  <h1>住客政策</h1>

  <h2>1. &nbsp;总则.</h2>

  <ol>
    <li>住客中至少必须有一位年满18周岁方可入住，且这一点必须在预订时向酒店方明确。
    </li>
    <li>14—18岁的未成年客人只有持经过公证的父母许可书方可独自入住酒店。
    </li>
    <li>&nbsp;14岁以上的客人入住时必须出示有效的身份证件。以下是有效的身份证件：俄联邦护照，外国护照，军人证，临时身份证，外国身份证。<strong>重要提示：驾照不属于身份证之列。</strong></li>
    <li>&nbsp;若住客中有一名2岁以下的儿童，则可以免除该孩童的房费，但其早餐是否收费则要由酒店方面决定。

    </li>
    <li>&nbsp;若住客需要加床，则酒店可以在收取附加费用的前提下提供额外的床垫。
    </li>
    <li>&nbsp;某些酒店可能会禁止携带宠物入住（参见酒店网站上“设施”部分的说明）。
    </li>
    <li>&nbsp;若住客的行为超出了住客权利的范围或违背了住客义务，则酒店有权对该住客采取必要措施。
    </li>
    <li>&nbsp;以优惠价格进行的预订不可以更改。 &nbsp;若享受优惠预订价的住客提前离店，则酒店方不会退回未住的房费。

    </li>
    <li>&nbsp;<strong>重要提示：</strong>某些酒店在一年的不同时间里可能有不同的住客政策。</li>
    <li>&nbsp;除设备老化折旧之外，一切由住客对酒店造成的损害均由住客负责。住客必须令房间处于干净整洁的良好状态。</li>
  </ol>

  <h2>2. &nbsp;住宿延长政策</h2>

  <ol>
    <li>若酒店有空闲房间，则住客可以按当前的房费支付额外费用，从而延长住宿时间。</li>
    <li>当前的房费可能会与预订时的房费不同。</li>
  </ol>

