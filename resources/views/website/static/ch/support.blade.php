    <p>如果需要一次性预订三间以上的房间，您可通过电话+7-812-309-84-45或邮箱help@roomp.co 联系我们。您必须预付总费用的25%才能确认订单，且需通过我们客服发送的特殊链接来支付多个房间的订单。.</p>
    <h3 name='can-i-book-at-a-third-party'>我能否用自己的账号为他人预订房间？</h3>

    <p>您可以用自己的账号为他人预订房间，但有一个要求：预订时提供的信息须与入住时提供给前台的信息相符。此外，订单一经确认，入住客人的姓名就无法更改。</p>
    <h3 name='can-i-book-at-a-few-hours'>能否预订钟点房？</h3>

    <p>酒店的房间至少必须预订一天。我们可以为您提供钟点房，但其价格也相当于普通房间一天的住宿费用。</p>
    
