 <p>Roomp并不保存或处理通过服务器获得的银行卡信息。您的银行卡信息由国际信息处理公司Payture进行处理。</p>
    
    <h3 name='can-i-get-a-discount-if-i-book-alone-but-i-book-for-two'>一人订双人间是否有折扣？</h3>

