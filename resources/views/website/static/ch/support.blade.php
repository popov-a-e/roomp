    <h3 name='why-roomp'>为什么要选择Roomp订房？它相比其他同类网站有什么优势？</h3>

    <p>Roomp承诺为每位预订的客人提供标准化的房间和服务，而这一点是其他同类平台所不具备的。我们还会定期审查各酒店和房间，确保住宿质量保持在较高水准。除此之外，我们的价格也是目前市场上最优惠的。</p>
    <h2>预订</h2>

    <h3 name='can-i-put-three-in-double'>我们有三人入住，能否只订一个双人间？</h3>

    <p>某些酒店允许多名客人同住一个房间。如果您在为多人预订单个房间时遇到问题，请通过电话、电邮、Whatsapp或Telegram同客服联系，我们会努力为您找到最佳解决方案。</p>
    <h3 name='what-is-standard-time-checkin-checkout'>入住和离店有什么时间规定？</h3>

    <p>多数情况下，标准的入住时间是14点之后，标准的离店时间是12点之前。不过，确切的入住时间和离店时间应在酒店网页和订单确认页面上注明，请以这个时间为准。</p>
    <h3 name='is-early-checkin-late-checkout-possible'>能否提前入住或推迟离店？</h3>

    <p>能否提前入住或推迟离店，取决于当时酒店有无多余的房间，且必须为此支付额外的房费。如果您有提前入住或推迟离店的需要，请在预订页面上专门留出的位置注明。但是，这并不能保证酒店一定会满足您的要求。详情可参见“住宿政策”。如果您还想进一步了解是否可能提前入住或推迟离店，请通过电话、邮箱、Whatsapp或Telegram联系我们的客服。</p>
    <h3 name='how-to-register'>入住时需要出示哪些身份证明？</h3>

    <p>以下是可在入住时出示的身份证件：俄联邦护照，外国护照，军人证，临时身份证，外国身份证。重要提示：驾照不属于身份证之列。</p>
    <h3 name='can-i-settle-with-pets'>能否携带宠物入住？</h3>

    <p>是否能携带宠物入住取决于不同酒店的规定。如果酒店网站在“设施”一栏下注明“可携带宠物”，则可以同宠物一起入住。</p>
    <h3 name='why-status-is-unconfirmed'>为什么我的预订状态变成了“未确认”？</h3>

    <p>这种情况可能是因为您没有完成预付。Roomp会通过短信或电话确认您的到达，但如果您没有及时回复或确认到达，未预付的订单就可能被取消。
    </p>
    <p>万一发生这种情况而您又想确认订单，请立刻同我们的客服联系。</p>
