    <h2>支付与保障</h2>

    <h3 name='how-to-cancel-a-booking'>如何支付订单？</h3>

    <p>您可以在预订时使用信用卡进行支付，或者在入住时进行到店支付。两种方式各有优劣。</p>
    <p>到店支付可以用现金或信用卡完成，因此具有一定的灵活性。但是，并非所有酒店都支持到店支付，三个房间以上的订单也不支持到店支付。</p>
    <p>不论您预订多少房间，您都可以在预订时进行在线支付。</p>
    
    <h3 name='why-some-hotels-lack-cash-pay'>为什么有些酒店不支持到店支付？</h3>

    <p>有些酒店的房间需求量很大，因此不提供到店支付。您可以从我们网站提供的网上支付方式中选择一种支付订单。</p>
    
    <h3 name='why-hotelier-changed-the-price'>为什么酒店收取的房费与预订时的房费不符？</h3>

